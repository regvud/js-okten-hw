let users = [
    {name: 'vasya', age: 31, status: false},
    {name: 'petya', age: 30, status: true},
    {name: 'kolya', age: 29, status: true},
    {name: 'olya', age: 28, status: false},
    {name: 'max', age: 30, status: true},
    {name: 'anya', age: 31, status: false},
    {name: 'oleg', age: 28, status: false},
    {name: 'andrey', age: 29, status: true},
    {name: 'masha', age: 30, status: true},
    {name: 'olya', age: 31, status: false},
    {name: 'max', age: 31, status: true}];

const favoritesArray = [];
const favoritesButton = document.getElementById('favs');

favoritesButton.onclick = function () {
    location.href = '../favorites/index.html'
}
users.forEach(user => {
    const wrapper = document.createElement('div');
    const info = document.createElement('span');
    const button = document.createElement('button');

    info.innerText = `${user.name} - ${user.age} - ${user.status}`
    button.innerText = 'add to fav'

    button.onclick = function () {
        favoritesArray.push(user)
        localStorage.setItem('favorites', JSON.stringify(favoritesArray))
    }

    wrapper.append(info, button)
    document.body.append(wrapper)
})